pub struct Player {
    pub x: f32,
    pub y: f32,
    pub a: f32,
}

impl Player {
    pub fn new() -> Self {
        Player {
            x: 30.0,
            y: 30.0,
            a: PI / 4.0,
        }
    }

    pub fn draw(&self, buf: &mut MyBuffer, color: u32) {
        buf.draw_rect(self.x as i32 - 1, self.y as i32 - 1, 3, 3, color);
    }
}

pub struct MyGameState {
    pub buf: MyBuffer,
    pub player: Player,
    pub map: Map,
}

impl MyGameState {
    pub fn new() -> Self {
        let buf = MyBuffer::new();
        let player = Player::new();
        let map = Map::new();

        MyGameState { buf, player, map }
    }
}
